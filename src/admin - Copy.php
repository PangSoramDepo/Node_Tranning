       if (x.val()==0)
       { 
           ethis.find('.slide-menu11').slideDown(200);
           ethis.find ('i').removeClass('fa-angle-right');
           ethis.find ('span').addClass('glyphicon glyphicon-user fas fa-angle-double-down'); 
