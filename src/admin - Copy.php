       {
            ethis.find('.slide-menu11').slideUp(200);
            ethis.find ('i').addClass('fas fa-angle-right');
          ethis.find ('span').removeClass();
            x.val(0);
           
            
          
           
       }
        
       
    });
        
    });

    

